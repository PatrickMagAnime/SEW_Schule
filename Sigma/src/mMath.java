public class mMath {

    // Method to calculate the power of a number
    public static double pow(double base, double exponent) {
        return Math.pow(base, exponent);
    }

    // Method to calculate the square root of a number
    public static double sqrt(double value) {
        return Math.sqrt(value);
    }

    // Method to calculate the absolute value of a number
    public static double abs(double value) {
        return Math.abs(value);
    }

    // Method to calculate the maximum of two numbers
    public static double max(double a, double b) {
        return Math.max(a, b);
    }

    // Method to calculate the minimum of two numbers
    public static double min(double a, double b) {
        return Math.min(a, b);
    }

    // Method to calculate the sine of an angle (in radians)
    public static double sin(double angle) {
        return Math.sin(angle);
    }

    // Method to calculate the cosine of an angle (in radians)
    public static double cos(double angle) {
        return Math.cos(angle);
    }

    // Method to calculate the tangent of an angle (in radians)
    public static double tan(double angle) {
        return Math.tan(angle);
    }

    // Method to calculate the natural logarithm (base e) of a number
    public static double log(double value) {
        return Math.log(value);
    }

    // Method to calculate the base 10 logarithm of a number
    public static double log10(double value) {
        return Math.log10(value);
    }

    // Method to calculate the exponential of a number (e^value)
    public static double exp(double value) {
        return Math.exp(value);
    }
}
